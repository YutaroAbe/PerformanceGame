//=============================================================================
// 初期化処理
//=============================================================================
void InitTutorial(void)
{
	// 背景初期化処理
	InitTutorialBG();
}
//=============================================================================
// 終了処理
//=============================================================================
void UninitTutorial(void)
{
	// 背景終了処理
	UninitTutorialBG();
}
//=============================================================================
// 更新処理
//=============================================================================
void UpdateTutorial(void)
{
	FADE *fade;
	fade = GetFade();

	// 背景更新処理
	UpdateTutorialBG();

	if (GetKeyboardTrigger(DIK_RETURN) == true && *fade == FADE_NONE)
	{
		SetFade(MODE_SELECT);
		PlaySound(SOUND_LABEL_SE_DICIDE);
	}
}
//=============================================================================
// 描画処理
//=============================================================================
void DrawTutorial(void)
{
	// 背景描画処理
	DrawTutorialBG();
}